        int cantidadColumnas=Funcion.buscarFilaColumnaCantidad(matrizJuego1,1);
        
        matrizBotones = new JButton[cantidadFilas][cantidadColumnas];
        initComponents();
        Conexion.crearConectadosProlog(matrizOriginalJuego1);
        
